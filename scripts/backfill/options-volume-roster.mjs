#!/usr/bin/env node
// options-volume-roster.mjs — generate src/data/options-volume-roster.json
// from a Barchart "stocks screener" CSV exported and dropped into
// C:\sheets\.
//
// The /heatmap surface tracks the top ~250 single-name stocks by US
// options volume, equal-sized within sector groups. The universe is
// intentionally not the SP500 (the project's vol-trader audience cares
// about the names they actually trade — NVDA, TSLA, MSTR, PLTR, IREN,
// MARA, HIMS, etc. — many of which are not SP500 members) and is
// not market-cap-weighted (the MAG7 dominate weight-by-market-cap so
// thoroughly that 5-7 tiles command a third of the visual; equal-
// size tiles within sector bands let every name in the universe
// command attention).
//
// Workflow:
//   1. Eric drops a fresh CSV from Barchart's stocks screener at
//      C:\sheets\stocks-screener-<date>.csv (filename pattern matches
//      what Barchart's export produces). CSV columns: Symbol, Name,
//      Exchange, Options Vol.
//   2. Run this script: node scripts/backfill/options-volume-roster.mjs
//      (auto-discovers the most recent matching file in C:\sheets\)
//   3. Script joins each ticker with a GICS sector. SP500 names get
//      their sector from the community-maintained GitHub
//      datasets/s-and-p-500-companies CSV (same source the SP500
//      roster generator uses). Non-SP500 names — recent IPOs, ADRs,
//      meme stocks, small-cap miners — fall through to a manually-
//      maintained MANUAL_SECTORS map keyed at the bottom of this
//      file. Anything still unmatched lands in sector "Other" with
//      an explicit log line so the gap is visible at refresh time
//      and the override map can be extended on the next run.
//   4. Output: src/data/options-volume-roster.json with shape
//      { generatedAt, sourceCsv, count, sectors, holdings: [...] }
//      where each holding carries {symbol, name, sector,
//      optionsVolume}.
//
// Manual map maintenance: when a new top-250 list adds a name not in
// the SP500 CSV and not in MANUAL_SECTORS, the script logs it to
// stderr and exits 1 (refusing to silently classify into "Other"
// would just push the problem downstream — better to catch it at
// roster generation than render the heatmap with a giant "Other"
// region). The override entry is two lines: a comment explaining
// the company and the GICS sector. Most non-SP500 options-active
// names are stable categorically (MSTR is always Information
// Technology, MARA is always Financials by GICS, IREN / RIOT are
// Information Technology). Drift in the membership is faster than
// drift in classification — most names that appear here once will
// appear again on every refresh.

import { readFileSync, writeFileSync, mkdirSync, readdirSync, statSync } from 'node:fs';
import { dirname, join } from 'node:path';
import process from 'node:process';

const DEFAULT_SHEETS_DIR = 'C:\\sheets';
// Filename pattern broad enough to match the two naming conventions
// observed in production: the Barchart export's default
// `stocks-screener-*.csv`, and the date-prefixed manual rename Eric
// uses (`MM-DD-YYYY (top 250 option volume).csv`). Both contain the
// hyphenated "option volume" or "stocks-screener" sub-string. The /i
// flag tolerates case drift in either convention. The mtime-sort below
// keeps the auto-discover stable when multiple matching CSVs land in
// the directory across consecutive refreshes.
const SHEETS_FILE_PATTERN = /(stocks-screener|option[\s_-]?volume)/i;
const SECTORS_CSV_URL = 'https://raw.githubusercontent.com/datasets/s-and-p-500-companies/main/data/constituents.csv';
const DEFAULT_OUT = 'src/data/options-volume-roster.json';
// Path to the companion sp500-roster.json generated by
// scripts/backfill/sp500-roster.mjs from SSGA's daily SPY holdings xlsx.
// Optional input — when present, every OV holding that intersects the
// SP500 picks up SP500 weight + market-cap rank, and the script computes
// derived `anchor` and `hype` per-holding fields. When absent, the
// generator skips those fields and writes the legacy shape.
const SP500_ROSTER_PATH = 'src/data/sp500-roster.json';
// Number of top OV-∩-SP500 names tagged as `anchor: true` in the
// payload. Selected by ascending harmonic mean of (ov_rank, mc_rank) so
// both axes have to be high — a heavyweight that isn't options-active
// (BRK.A, BLK at the margin) gets excluded, and a hype-driven name
// without market cap (DJT, SBET) gets excluded. The 122-name OV ∩
// SP500 intersection (measured 2026-04-27) leaves comfortable
// headroom for a 50-name anchor list.
const ANCHOR_LIST_SIZE = 50;

const log = (event, data = {}) =>
  console.log(JSON.stringify({ ts: new Date().toISOString(), event, ...data }));

function parseArgs(argv) {
  const out = { csvPath: null, sheetsDir: DEFAULT_SHEETS_DIR, outPath: DEFAULT_OUT };
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === '--csv') out.csvPath = argv[++i];
    else if (a === '--sheets-dir') out.sheetsDir = argv[++i];
    else if (a === '--out') out.outPath = argv[++i];
  }
  return out;
}

function findLatestCsv(dir) {
  const entries = readdirSync(dir);
  const matches = entries
    .filter((name) => SHEETS_FILE_PATTERN.test(name))
    .map((name) => {
      const full = join(dir, name);
      const st = statSync(full);
      return { full, name, mtime: st.mtimeMs };
    })
    .sort((a, b) => b.mtime - a.mtime);
  return matches[0]?.full ?? null;
}

function parseCsv(text) {
  const lines = text.split(/\r?\n/).filter((l) => l.length > 0);
  if (lines.length === 0) return { header: [], rows: [] };
  const splitLine = (line) => {
    const out = [];
    let field = '';
    let inQ = false;
    for (let i = 0; i < line.length; i++) {
      const ch = line[i];
      if (inQ) {
        if (ch === '"') { inQ = false; continue; }
        field += ch;
      } else {
        if (ch === '"') { inQ = true; continue; }
        if (ch === ',') { out.push(field); field = ''; continue; }
        field += ch;
      }
    }
    out.push(field);
    return out;
  };
  const header = splitLine(lines[0]);
  const rows = lines.slice(1).map((l) => {
    const cells = splitLine(l);
    const row = {};
    for (let i = 0; i < header.length; i++) row[header[i]] = (cells[i] ?? '').trim();
    return row;
  });
  return { header, rows };
}

async function fetchSp500SectorMap() {
  const res = await fetch(SECTORS_CSV_URL, {
    signal: AbortSignal.timeout(15000),
    headers: { 'User-Agent': 'aigamma-options-roster/1.0' },
  });
  if (!res.ok) throw new Error(`sectors CSV HTTP ${res.status}`);
  const text = await res.text();
  const { rows } = parseCsv(text);
  const map = new Map();
  for (const r of rows) {
    const sym = String(r['Symbol'] ?? '').trim().toUpperCase();
    const sector = String(r['GICS Sector'] ?? '').trim();
    if (sym && sector) map.set(sym, sector);
  }
  return map;
}

async function main() {
  const args = parseArgs(process.argv.slice(2));
  const csvPath = args.csvPath || findLatestCsv(args.sheetsDir);
  if (!csvPath) {
    log('roster.no_csv', { sheetsDir: args.sheetsDir });
    process.exit(1);
  }
  log('roster.start', { csv: csvPath, out: args.outPath });

  const csvText = readFileSync(csvPath, 'utf8');
  const { rows } = parseCsv(csvText);
  if (rows.length === 0) {
    log('roster.csv_empty');
    process.exit(1);
  }
  log('roster.csv_parsed', { rows: rows.length });

  let sp500Sectors;
  try {
    sp500Sectors = await fetchSp500SectorMap();
    log('roster.sectors_fetched', { count: sp500Sectors.size });
  } catch (err) {
    log('roster.sectors_failed', { error: String(err) });
    process.exit(1);
  }

  const holdings = [];
  const unclassified = [];
  for (const r of rows) {
    const symbol = String(r['Symbol'] ?? '').trim().toUpperCase();
    if (!symbol) continue;
    const name = String(r['Name'] ?? '').trim();
    const optionsVolume = Number(String(r['Options Vol'] ?? '').replace(/,/g, '')) || 0;
    const sector = sp500Sectors.get(symbol)
      || MANUAL_SECTORS[symbol]
      || null;
    if (!sector) {
      unclassified.push({ symbol, name });
      continue;
    }
    holdings.push({ symbol, name, sector, optionsVolume });
  }

  if (unclassified.length > 0) {
    log('roster.unclassified', {
      count: unclassified.length,
      names: unclassified.map((u) => `${u.symbol} (${u.name})`),
    });
    log('roster.next_step',
      'Add the unclassified tickers to MANUAL_SECTORS at the bottom of this script and re-run. ' +
      'Use the eleven canonical GICS sector names: Information Technology, Health Care, Financials, ' +
      'Communication Services, Consumer Discretionary, Consumer Staples, Industrials, Energy, ' +
      'Utilities, Real Estate, Materials.');
    process.exit(1);
  }

  // Sort holdings by options volume descending so the JSON is stable
  // and the function can rely on roster order matching the CSV's rank.
  holdings.sort((a, b) => b.optionsVolume - a.optionsVolume);

  // Per-holding ov_rank from the sort order — 1-indexed so the rank-1
  // (most options-active) name is at the top of the list.
  for (let i = 0; i < holdings.length; i++) {
    holdings[i].ovRank = i + 1;
  }

  // Optional SP500 enrichment. The sp500-roster.json file (regenerated
  // by `node scripts/backfill/sp500-roster.mjs` from SSGA's daily SPY
  // xlsx, via the same SECURITY_DEFINER GICS-CSV join used above) is
  // shipped at src/data/. When the file is present we merge in
  // market-cap weight + market-cap rank for every OV name that
  // intersects the SP500, and derive two new fields per holding:
  //
  //   - `weight` (number | null) — float-adjusted SP500 weight (NVDA
  //     ≈ 7.96%, AAPL ≈ 6.59%, …). `null` for non-SP500 names.
  //   - `mcRank` (int | null) — 1-indexed rank within SP500 by weight.
  //   - `hype` (int | null) — `ovRank - mcRank`. Negative values mean
  //     options activity is way ahead of fundamental size (DJT-class:
  //     options-trader hype without market cap to anchor it). Values
  //     near zero mean OV rank tracks size rank cleanly (NVDA-class:
  //     fundamental options-trader name). Null when not in SP500.
  //   - `anchor` (bool) — true for the top ANCHOR_LIST_SIZE (50) names
  //     ranked by ascending harmonic mean of (ovRank, mcRank). Both
  //     axes must be high to win — a name with great OV but poor MC
  //     rank loses to a name with merely-decent on both. Only
  //     SP500-intersect names can be anchors by construction.
  //
  // When sp500-roster.json is absent (e.g. fresh checkout, or a CI
  // run that hasn't materialized the file yet), the generator gracefully
  // skips the SP500 fields and writes the legacy shape. The OV roster
  // remains usable in that mode for the bulk universe-scoping that
  // heatmap.mjs / scan.mjs / earnings.mjs / stock-performance.mjs
  // already depend on.
  let sp500Map = new Map();
  let sp500AsOf = null;
  try {
    const sp500Json = JSON.parse(readFileSync(SP500_ROSTER_PATH, 'utf8'));
    sp500AsOf = sp500Json.asOf || null;
    // The sp500 generator already sorts holdings descending by weight,
    // so iteration order yields rank 1-indexed in weight-descending order.
    sp500Json.holdings.forEach((h, i) => {
      const sym = String(h.symbol || '').trim().toUpperCase();
      if (!sym) return;
      const w = Number(h.weight);
      if (!Number.isFinite(w)) return;
      sp500Map.set(sym, { weight: +w.toFixed(4), mcRank: i + 1 });
    });
    log('roster.sp500_merged', { sp500Count: sp500Map.size, sp500AsOf });
  } catch (err) {
    log('roster.sp500_skipped', { reason: String(err.code || err.message || err) });
  }

  // Per-holding SP500 enrichment + hype derivation. Anchor is computed
  // in a second pass below because it depends on the joint rank scoring.
  for (const h of holdings) {
    const spx = sp500Map.get(h.symbol);
    h.weight = spx ? spx.weight : null;
    h.mcRank = spx ? spx.mcRank : null;
    h.hype = spx ? h.ovRank - spx.mcRank : null;
  }

  // Anchor 50 selection — top-N OV ∩ SP500 names by ascending harmonic
  // mean of (ovRank, mcRank). The harmonic mean punishes either axis
  // being weak: a name that is OV-rank 1 but mc-rank 200 scores 1.99
  // (close to its weak axis), while a name that is ovRank 30 and
  // mcRank 30 scores 30 (matches its uniform axes). Both extremes lose
  // to NVDA-class names where both ranks are near 1. Names not in
  // SP500 are skipped — they cannot be anchors regardless of OV
  // rank, because the data spine for "is this a permanent core name"
  // is the SP500 weight column. The set is a small fixed K so we
  // don't need a heap; sort the candidates and slice.
  const anchorCandidates = holdings
    .filter((h) => h.mcRank != null)
    .map((h) => ({
      symbol: h.symbol,
      score: (2 * h.ovRank * h.mcRank) / (h.ovRank + h.mcRank),
    }))
    .sort((a, b) => a.score - b.score);
  const anchorSet = new Set(
    anchorCandidates.slice(0, ANCHOR_LIST_SIZE).map((c) => c.symbol),
  );
  for (const h of holdings) {
    h.anchor = anchorSet.has(h.symbol);
  }

  const sectorCounts = {};
  for (const h of holdings) sectorCounts[h.sector] = (sectorCounts[h.sector] || 0) + 1;

  const anchorCount = holdings.filter((h) => h.anchor).length;
  const sp500IntersectCount = holdings.filter((h) => h.mcRank != null).length;

  const payload = {
    generatedAt: new Date().toISOString(),
    sourceCsv: csvPath,
    sp500AsOf,
    count: holdings.length,
    sp500IntersectCount,
    anchorCount,
    sectors: sectorCounts,
    holdings,
  };

  mkdirSync(dirname(args.outPath), { recursive: true });
  writeFileSync(args.outPath, JSON.stringify(payload, null, 2) + '\n', 'utf8');
  log('roster.done', {
    out: args.outPath,
    count: holdings.length,
    sp500IntersectCount,
    anchorCount,
    sectorCounts,
    top5: holdings.slice(0, 5).map((h) => `${h.symbol}=${h.optionsVolume}`),
    anchorTop10: holdings.filter((h) => h.anchor).slice(0, 10).map((h) =>
      `${h.symbol}(ov${h.ovRank}/mc${h.mcRank})`
    ),
  });
}

